package Pesantren;

public class Santri {
    private String nis;
    private String nama;
    private String jenisKelamin;
    private String ttlDaerah;
    private String dateChooser;
    private String alamat;
    private String ayah;
    private String ibu;

    public String getNis() {
        return nis;
    }

    public void setNis(String nis) {
        this.nis = nis;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJenisKelamin() {
        return jenisKelamin;
    }

    public void setJenisKelamin(String jenisKelamin) {
        this.jenisKelamin = jenisKelamin;
    }

    public String getTtlDaerah() {
        return ttlDaerah;
    }

    public void setTtlDaerah(String ttlDaerah) {
        this.ttlDaerah = ttlDaerah;
    }

    public String getDateChooser() {
        return dateChooser;
    }

    public void setDateChooser(String dateChooser) {
        this.dateChooser = dateChooser;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getAyah() {
        return ayah;
    }

    public void setAyah(String ayah) {
        this.ayah = ayah;
    }

    public String getIbu() {
        return ibu;
    }

    public void setIbu(String ibu) {
        this.ibu = ibu;
    }
    
    
}
